#include "STD_Types.h"
#include "BitOperations.h"

#include "GIE_Register.h"
#include "GIE_Interface.h"

void GIE_Enable (void)
{
	SET_BIT (SREG, SREG_I);
}

void GIE_Disable (void)
{
	CLR_BIT (SREG, SREG_I);
}